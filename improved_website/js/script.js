function filterCards() {
    const searchInput = document.getElementById("searchInput");
    const typeFilter = document.getElementById("typeFilter");
    const countryFilter = document.getElementById("countryFilter");
    const cards = document.querySelectorAll(".card");

    const searchTerm = (searchInput?.value || "").trim();
    const selectedType = typeFilter?.value || "all";
    const selectedCountry = countryFilter?.value || "all";

    const totalCards = cards.length;
    let visibleCount = 0;

    cards.forEach(card => {
        const haystack = getCardSearchHaystack(card);
        const cardType = card.getAttribute("data-type") || "other";
        const cardCountry = card.getAttribute("data-country") || "international";

        const matchesSearch = searchQueryMatchesHaystack(haystack, searchTerm);
        const matchesType = selectedType === "all" || cardType === selectedType;
        const matchesCountry = selectedCountry === "all" || cardCountry === selectedCountry;

        if (matchesSearch && matchesType && matchesCountry) {
            card.style.display = "";
            card.style.animation = "fadeIn 0.3s ease";
            visibleCount++;
        } else {
            card.style.display = "none";
        }
    });

    updateSearchInfo();

    const noResults = document.getElementById("noResults");
    if (noResults) {
        const showEmptyState = totalCards > 0 && visibleCount === 0;
        noResults.classList.toggle("visible", showEmptyState);
    }

    updateClearFiltersVisibility();
}

function updateSearchInfo() {
    const searchInput = document.getElementById("searchInput");
    const typeFilter = document.getElementById("typeFilter");
    const countryFilter = document.getElementById("countryFilter");
    const resultsCount = document.getElementById("resultsCount");
    const activeFilters = document.getElementById("activeFilters");
    const cards = document.querySelectorAll(".card");

    const language = localStorage.getItem("edugateway-language") || "sq";
    const totalCards = cards.length;

    // Count visible cards
    let visibleCount = 0;
    cards.forEach(card => {
        if (card.style.display !== "none") {
            visibleCount++;
        }
    });

    // Update results count (hide when this page has no opportunity cards)
    if (resultsCount) {
        const searchInfoEl = document.getElementById("searchInfo");
        if (totalCards === 0) {
            if (searchInfoEl) searchInfoEl.style.display = "none";
        } else {
            if (searchInfoEl) searchInfoEl.style.display = "";
            const showingText = translateText("Showing", language);
            const ofText = translateText("of", language);
            const opportunitiesText = translateText("opportunities", language);
            const resultsText = translateText("results", language);

            resultsCount.innerHTML = `${showingText} <strong>${visibleCount}</strong> ${resultsText} ${ofText} <strong>${totalCards}</strong> ${opportunitiesText}`;
        }
    }

    // Update active filters badges
    if (activeFilters) {
        let badgesHTML = "";

        if (searchInput?.value) {
            badgesHTML += `
                <span class="filter-badge">
                    "${searchInput.value}"
                    <button onclick="clearSearchFilter()">&times;</button>
                </span>
            `;
        }

        if (typeFilter?.value && typeFilter.value !== "all") {
            const typeKeyMap = {
                scholarship: "Scholarships",
                program: "Programs",
                other: "Other"
            };
            const typeKey = typeKeyMap[typeFilter.value] || typeFilter.value;
            const typeLabel = translateText(typeKey, language);
            badgesHTML += `
                <span class="filter-badge">
                    ${typeLabel}
                    <button onclick="clearTypeFilter()">&times;</button>
                </span>
            `;
        }

        if (countryFilter?.value && countryFilter.value !== "all") {
            const opt = countryFilter.options[countryFilter.selectedIndex];
            const countryKey = opt.getAttribute("data-i18n") || opt.textContent.trim();
            const countryLabel = translateText(countryKey, language);
            badgesHTML += `
                <span class="filter-badge">
                    ${countryLabel}
                    <button onclick="clearCountryFilter()">&times;</button>
                </span>
            `;
        }

        activeFilters.innerHTML = badgesHTML;
    }
}

