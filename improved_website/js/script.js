let searchTimeout;

function normalizeSearchString(str) {
    if (str == null) return "";
    let s = String(str).toLowerCase();
    try {
        s = s.normalize("NFD").replace(/\p{M}/gu, "");
    } catch (e) {
        s = s.normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    }
    return s.replace(/\s+/g, " ").trim();
}

function collectCardI18nRawText(card) {
    const chunks = [];
    card.querySelectorAll("[data-i18n]").forEach(el => {
        const raw = el.getAttribute("data-i18n");
        if (!raw) return;
        chunks.push(raw.replace(/\|/g, " "));
    });
    return chunks.join(" ");
}

function getCardSearchHaystack(card) {
    const visible = card.innerText || "";
    const keys = collectCardI18nRawText(card);
    return normalizeSearchString(`${visible} ${keys}`);
}

function searchQueryMatchesHaystack(haystackNorm, queryRaw) {
    const q = normalizeSearchString(queryRaw);
    if (!q) return true;
    const tokens = q.split(" ").filter(Boolean);
    const haystackWords = haystackNorm.split(" ");
    return tokens.every(token => 
        haystackNorm.includes(token) || 
        haystackWords.some(word => word.includes(token))
    );
}

