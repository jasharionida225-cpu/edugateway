function initializeTranslation() {
    const languageSelect = document.getElementById("languageSelect");
    if (!languageSelect) return;

    // Load saved language preference
    const savedLanguage = localStorage.getItem("edugateway-language") || "sq";
    languageSelect.value = savedLanguage;
    applyTranslation(savedLanguage);

    languageSelect.addEventListener("change", function() {
        const language = this.value;
        localStorage.setItem("edugateway-language", language);
        applyTranslation(language);
    });
}

function applyTranslation(language) {
    document.documentElement.lang = language === "en" ? "en" : "sq";

    const pageTitleKey = document.body?.getAttribute("data-i18n-page-title");
    if (pageTitleKey) {
        document.title = translateText(pageTitleKey, language);
    }

    document.querySelectorAll("[data-i18n-aria]").forEach(el => {
        const key = el.getAttribute("data-i18n-aria");
        if (key) {
            el.setAttribute("aria-label", translateText(key.trim(), language));
        }
    });

    // 1. All [data-i18n] except card body / card buttons / card type badges (handled in translateCards)
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (!key) return;
        if (el.tagName === "OPTION") {
            return;
        }
        const card = el.closest(".card");
        if (card) {
            const tag = el.tagName;
            if (tag === "P" || tag === "BUTTON" || el.classList.contains("card-type")) {
                return;
            }
        }
        el.textContent = translateText(key, language);
    });

    // 2. Input placeholders
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (key) el.placeholder = translateText(key, language);
    });

    // 3. Select options (filter + language)
    document.querySelectorAll(".filter-select option[data-i18n], #languageSelect option[data-i18n]").forEach(option => {
        const key = option.getAttribute("data-i18n");
        if (key) option.textContent = translateText(key, language);
    });

    translateCards(language);
    updateNoResultsMessages(language);
    // FIXED: Ensure filterCards runs reliably AFTER translation/DOM changes complete
    // Multiple rAF + setTimeout for safety across browsers
    requestAnimationFrame(() => {
        requestAnimationFrame(() => filterCards());
        setTimeout(filterCards, 100);
    });
}

function translateCards(language) {
    document.querySelectorAll(".card").forEach(card => {
        const typeBadge = card.querySelector(".card-type");
        if (typeBadge && typeBadge.hasAttribute("data-i18n")) {
            typeBadge.textContent = translateText(typeBadge.getAttribute("data-i18n"), language);
        }

        const paragraph = card.querySelector(":scope > p");
        if (paragraph && paragraph.hasAttribute("data-i18n")) {
            const original = paragraph.getAttribute("data-i18n");
            const parts = original.split("|");
            let translatedHTML = translateText(parts[0].trim(), language);

            if (parts.length > 1) {
                for (let i = 1; i < parts.length; i++) {
                    const raw = parts[i].trim();
                    const translatedPart = translateText(raw, language);
                    if (raw.endsWith(":")) {
                        translatedHTML += "<br><br><strong>" + translatedPart + "</strong>";
                    } else {
                        translatedHTML += "<br>• " + translatedPart;
                    }
                }
            }

            paragraph.innerHTML = translatedHTML.replace(/\n/g, "<br>");
        }

        const button = card.querySelector("button");
        if (button && button.hasAttribute("data-i18n")) {
            button.textContent = translateText(button.getAttribute("data-i18n"), language);
        }
    });
}

