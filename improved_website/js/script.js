function initializeKeyboardNav() {
    document.addEventListener("keydown", (e) => {
        // Focus search with Ctrl/Cmd + K
        if ((e.ctrlKey || e.metaKey) && e.key === "k") {
            e.preventDefault();
            const searchInput = document.getElementById("searchInput");
            if (searchInput) searchInput.focus();
        }

        if (e.key === "Escape") {
            if (typeof closeNotificationsDrawer === "function" && closeNotificationsDrawer()) {
                return;
            }
            const searchInput = document.getElementById("searchInput");
            if (searchInput && searchInput.value) {
                searchInput.value = "";
                syncSearchClearButton();
                filterCards();
            }
        }
    });
}

/* ==================== NOTIFICATIONS (collapsible tray) ==================== */
let notificationsDrawerOpen = false;

function setNotificationsDrawerOpen(open) {
    const root = document.getElementById("notificationsRoot");
    const panel = document.getElementById("notificationsPanel");
    const toggle = document.getElementById("notificationsToggle");
    if (!root) return;
    if (open && root.hidden) return;

    notificationsDrawerOpen = open;
    root.classList.toggle("notifications-root--open", open);
    document.body.classList.toggle("notifications-drawer-open", open);

    if (toggle) {
        toggle.setAttribute("aria-expanded", open ? "true" : "false");
    }
    if (panel) {
        panel.setAttribute("aria-hidden", open ? "false" : "true");
    }

    if (open) {
        const closeBtn = document.getElementById("notificationsDrawerClose");
        window.requestAnimationFrame(() => {
            (closeBtn || toggle)?.focus();
        });
    } else if (toggle && document.activeElement && panel?.contains(document.activeElement)) {
        toggle.focus();
    }
}

/** @returns {boolean} true if drawer was open and is now closed */
function closeNotificationsDrawer() {
    if (!notificationsDrawerOpen) return false;
    setNotificationsDrawerOpen(false);
    return true;
}

function updateNotificationsPanelLayout() {
    const root = document.getElementById("notificationsRoot");
    const panel = document.getElementById("notificationsPanel");
    const badge = document.getElementById("notificationsBadge");

    document.body.classList.remove("has-notifications");

    if (!root || !panel) {
        return;
    }

    const visibleCards = Array.from(panel.querySelectorAll(".notification-card")).filter(
        (c) => !c.hidden
    );
    const count = visibleCards.length;

    if (count === 0) {
        setNotificationsDrawerOpen(false);
    }

    root.hidden = count === 0;

    if (badge) {
        badge.textContent = count > 9 ? "9+" : String(count);
    }
}

function initializeNotificationsPanel() {
    const root = document.getElementById("notificationsRoot");
    const panel = document.getElementById("notificationsPanel");
    const toggle = document.getElementById("notificationsToggle");
    const backdrop = document.getElementById("notificationsBackdrop");
    const closeDrawerBtn = document.getElementById("notificationsDrawerClose");

    if (!root || !panel) return;

    panel.querySelectorAll(".notification-card__close").forEach(btn => {
        btn.addEventListener("click", () => {
            const card = btn.closest(".notification-card");
            if (!card) return;
            const id = card.getAttribute("data-notification-id");
            if (id) {
                try {
                    sessionStorage.setItem(`eg-notification-dismissed-${id}`, "1");
                } catch (e) {
                    /* ignore */
                }
            }
            card.classList.add("notification-card--dismissed");
            setTimeout(() => {
                card.hidden = true;
                updateNotificationsPanelLayout();
            }, 280);
        });
    });

    panel.querySelectorAll(".notification-card[data-notification-id]").forEach(card => {
        const id = card.getAttribute("data-notification-id");
        if (!id) return;
        try {
            if (sessionStorage.getItem(`eg-notification-dismissed-${id}`) === "1") {
                card.hidden = true;
                card.classList.add("notification-card--dismissed");
            }
        } catch (e) {
            /* ignore */
        }
    });

    updateNotificationsPanelLayout();

    if (toggle) {
        toggle.addEventListener("click", () => {
            if (root.hidden) return;
            setNotificationsDrawerOpen(!notificationsDrawerOpen);
        });
    }

    if (backdrop) {
        backdrop.addEventListener("click", () => setNotificationsDrawerOpen(false));
    }

    if (closeDrawerBtn) {
        closeDrawerBtn.addEventListener("click", () => setNotificationsDrawerOpen(false));
    }

    let touchStartX = 0;
    panel.addEventListener(
        "touchstart",
        (e) => {
            touchStartX = e.changedTouches[0].screenX;
        },
        { passive: true }
    );
    panel.addEventListener(
        "touchend",
        (e) => {
            const endX = e.changedTouches[0].screenX;
            if (endX - touchStartX > 56) {
                setNotificationsDrawerOpen(false);
            }
        },
        { passive: true }
    );
}

